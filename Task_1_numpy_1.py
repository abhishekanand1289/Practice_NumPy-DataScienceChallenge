def date_array(start:str, end:str):
  if type(start) != str or type(end) != str:
    raise ValueError("Takes only string parameters")
  else:
    end = end.split('-')
    end[-1] = int(end[-1])+1
    end[-1] = f'0{end[-1]}' if end[-1] < 10 else f'{end[-1]}'
    end1 = "-".join(end)
    return np.arange(start, end1, dtype = np.datetime64)

